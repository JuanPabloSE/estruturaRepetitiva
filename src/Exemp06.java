public class Exemp06 {
    public static void main(String[] args) {

        // Contextualização: Você quer somar todos os números de 1 a 5 para calcular o
        //total de pontos em um jogo.

        int soma = 0;

        for (int i = 1; i <= 5; i++) {
            soma += i;
        }

        System.out.printf("Soma total: %d\n", soma);

    }
}
